// ─── helpers ───────────────────────────────────────────────
function slug(title: string): string {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

let _vid = 0;
function vid() {
  _vid += 1;
  return `local-variant-${_vid}`;
}

